import React, { Component } from 'react'

class Field extends Component{
    render(){
        return(
            <div style={{background:"yellow"}}>
                <label>{this.props.label}</label>
                <input type={this.props.type} onChange={(event)=>{
                    // console.log(event.target.value)
                    this.props.onChangeEvent(event.target.value)
                }} value={this.props.value}/>
            </div>
        )
    }
}

export default class App extends Component {
    state={
        username:localStorage.getItem("username"),
        password:""
    }
  render() {
    return (
      <div>
        <h1>login</h1>
        <Field label="username" type="text" onChangeEvent={(value)=>{
            // console.log(value)
            this.setState({
                username:value
            })
        }} value={this.state.username}/>
        <Field label="password" type="password" onChangeEvent={(value)=>{
            this.setState({
                password:value
            })
        }} value={this.state.password} />

        <button onClick={()=>{
            console.log(this.state.username, this.state.password, 'enviar para back-end')
        }}>login</button>
        <button onClick={()=>{
            this.setState({
                username:"",
                password:""
            })
        }}>cancela</button>
      </div>
    )
  }
}
